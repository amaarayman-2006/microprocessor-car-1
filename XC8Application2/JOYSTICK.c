#include "JOYSTICK.h"

/* =====================================================================
 *  JOYSTICK.c — تنفيذ ميكنة قراءة الجويستيك (XY + زرار) على الـ Arduino UNO
 *
 *  ملاحظة مهمّة:
 *  ----------------
 *  الـ ADC لازم يكون مهيّأ (initialised) قبل ما تستدعي أي API بيقرأ.
 *  دالة JOYSTICK_INIT() بنفسها بتعمل adc_init() جوّاها، فالتطبيق محتاج
 *  بس يستدعيها مرة واحدة في البداية وخلاص.
 * ===================================================================== */

void JOYSTICK_INIT(void)
{
    /* --------------------------------------------------------------
     * تهيئة الـ ADC:
     *   - مرجع الجهد:  AVCC  (≈ 5V من الـ USB)
     *   - الـ Prescaler: /128
     *
     *   عند تردد 16 MHz (كريستال الـ UNO):  16M / 128 = 125 kHz  ✅
     *   عند تردد  8 MHz (RC داخلي):          8M / 128 =  62.5 kHz ✅
     *
     *   الاتنين بيقعوا داخل المدى المطلوب (50..200 kHz)
     *   عشان الـ ADC يطلّعلك دقّة 10-bit كاملة.
     * -------------------------------------------------------------- */
    adc_init(AVCC, DIV_128);

    /* --------------------------------------------------------------
     * زرار الجويستيك (SW):
     *   - مدخل (INPUT)
     *   - مع تفعيل الـ Pull-up الداخلي (Active-LOW)
     *
     *   على الـ ATmega328P: لما الـ DDRx = 0 (مدخل) وكتبت 1 على الـ PORTx
     *   بيتفعل الـ Pull-up الداخلي تلقائيًا، وده اللي بنعمله هنا.
     * -------------------------------------------------------------- */
    GPIO_SetPinDirection(JOYSTICK_PB_PORT, JOYSTICK_PB_PIN, GPIO_INPUT);
    GPIO_WritePin       (JOYSTICK_PB_PORT, JOYSTICK_PB_PIN, GPIO_HIGH);
}

/* =====================================================================
 *  JOYSTICK_READ_RAW
 *  --------------------------------------------------------------------
 *  بترجع القراءة الخام (0..1023) للمحورين X و Y.
 *  بنحط القراءة في الـ struct اللي اليوزر بعت ليها بوينتر.
 *  لو البوينتر = NULL بنخرج بدون ما نعمل أي حاجة (حماية).
 * ===================================================================== */
void JOYSTICK_READ_RAW(Joystick_RawData_t *pData)
{
    if (pData == ((void*)0)) return;     /* حماية: لو البوينتر فاضي اخرج */

    /* بنقرا المحورين بالترتيب — كل قراءة لقناة ADC مختلفة */
    pData->x = analogRead(JOYSTICK_X_CH);   /* A0 = PC0 = ADC0 → محور X */
    pData->y = analogRead(JOYSTICK_Y_CH);   /* A1 = PC1 = ADC1 → محور Y */
}

/* =====================================================================
 *  JOYSTICK_GET_DIR
 *  --------------------------------------------------------------------
 *  دي الفنكشن اللي بتقرر "الجويستيك دلوقتي راجع منين؟".
 *
 *  الفكرة (9-zone):
 *    بنقسم كل محور لـ 3 مناطق:  LOW (شمال/ورا) ، MID (راحة) ، HIGH (يمين/قدام)
 *    اللي بيدّينا 3×3 = 9 احتمالات (4 أساسي + 4 قطري + الراحة).
 *
 *  بنستخدم Booleans عشان الكود يبقى نضيف وقابل للقراءة:
 *    fwd → الجويستيك مدفوع لقدام
 *    bwd → الجويستيك مدفوع لورا
 *    lft → الجويستيك مدفوع شمال
 *    rgt → الجويستيك مدفوع يمين
 *
 *  بعد كده بنطلع التركيبة المظبوطة من الـ enum.
 *
 *  مهم: بنستخدم 9-zone عشان الحركة القطرية تشتغل صح،
 *  مش زي القديم اللي كان بيدّي الأولوية لمحور Y فبيلغي القطرية.
 * ===================================================================== */
Joystick_Direction_t JOYSTICK_GET_DIR(void)
{
    Joystick_RawData_t raw;
    JOYSTICK_READ_RAW(&raw);

    /* خرائط البولين: كل واحدة 1 لما الجويستيك مدفوع في الاتجاه ده */
    u8 fwd = (raw.y >= JOYSTICK_HIGH_THRESH) ? 1u : 0u;   /* محور Y عالي → قدام */
    u8 bwd = (raw.y <= JOYSTICK_LOW_THRESH ) ? 1u : 0u;   /* محور Y منخفض → ورا */
    u8 rgt = (raw.x >= JOYSTICK_HIGH_THRESH) ? 1u : 0u;   /* محور X عالي → يمين */
    u8 lft = (raw.x <= JOYSTICK_LOW_THRESH ) ? 1u : 0u;   /* محور X منخفض → شمال */

    /* ---------- الاتجاهات القُطرية الأربعة (الأولوية الأولى) ---------- */
    if (fwd && lft) return JOYSTICK_DIR_FORWARD_LEFT;     /* قدام + شمال */
    if (fwd && rgt) return JOYSTICK_DIR_FORWARD_RIGHT;    /* قدام + يمين */
    if (bwd && lft) return JOYSTICK_DIR_BACKWARD_LEFT;    /* ورا  + شمال */
    if (bwd && rgt) return JOYSTICK_DIR_BACKWARD_RIGHT;   /* ورا  + يمين */

    /* ---------- الاتجاهات الأساسية الأربعة ---------- */
    if (fwd) return JOYSTICK_DIR_FORWARD;
    if (bwd) return JOYSTICK_DIR_BACKWARD;
    if (lft) return JOYSTICK_DIR_LEFT;
    if (rgt) return JOYSTICK_DIR_RIGHT;

    /* ---------- داخل منطقة الراحة → وقوف ---------- */
    return JOYSTICK_DIR_STOP;
}

/* =====================================================================
 *  JOYSTICK_IsBtnPressed
 *  --------------------------------------------------------------------
 *  بنرجّع 1 لو الزرار مضغوط، 0 لو لأ.
 *  الزرار Active-LOW (لما تضغطه بيوصل الأرض → القراءة = 0).
 * ===================================================================== */
u8 JOYSTICK_IsBtnPressed(void)
{
    return (GPIO_ReadPin(JOYSTICK_PB_PORT, JOYSTICK_PB_PIN) == GPIO_LOW) ? 1u : 0u;
}
