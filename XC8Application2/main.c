/* =====================================================================
 *  main.c — البرنامج الرئيسي لعربية روبوت بـ ATmega328P
 *           (نفس شريحة الـ Arduino UNO — DIP-28)
 *
 *  معمارية الكود (Layered Architecture):
 *  --------------------------------------
 *      MCAL : GPIO_*  +  درايڤر الـ ADC (interface/private/program)
 *      HAL  : MOTOR  +  JOYSTICK  +  BATTERY  +  LED
 *      APP  : الفايل ده
 *
 *  السلوك العام (Behaviour):
 *  ------------------------
 *    1) نهيّأ الـ ADC + موديلات الـ HAL.
 *    2) في اللوب الرئيسي:
 *         أ) نقرا الجويستيك ونحدّث الموتورين على أساسه.
 *         ب) نسامبل البطارية، ولو ضعيفة نولّع الـ LED الأحمر.
 *
 *  ليه فيه BATT_CHECK_DIVIDER؟
 *  ----------------------------
 *  قراءة البطارية بتاخد قراءة ADC إضافية كل لفة، ودي بتاخد وقت.
 *  بدل ما نعمل ده كل لفة ونبطّأ استجابة الجويستيك، بنقراها كل
 *  ~50 لفة بس، ودي عملياً معدل أكتر من الكفاية لمراقبة بطارية.
 * ===================================================================== */

#include "MOTOR.h"
#include "JOYSTICK.h"
#include "BATTERY.h"
#include "LED.h"
#include "interface.h"   /* adc_init */

/* بنشيك على البطارية كل ~50 لفة من الـ control loop */
#define BATT_CHECK_DIVIDER   50u

int main(void)
{
    /* -------------- تهيئة طبقة الـ MCAL --------------
     * الـ ADC بيستخدمه كل من الجويستيك والبطارية،
     * فبنهيّأه مرة واحدة هنا في الأول.
     *   - مرجع الجهد:  AVCC (5V)
     *   - Prescaler:   /128 (يدّينا 125 kHz عند 16 MHz)
     */
    adc_init(AVCC, DIV_128);

    /* -------------- تهيئة طبقة الـ HAL -------------- */
    MOTOR_Init();        /* بنّات الموتورين Output + إيقاف */
    JOYSTICK_INIT();     /* تجهيز زر الجويستيك (وبتعمل adc_init تاني — مفيش ضرر) */
    BATTERY_Init();      /* تصفير مزلاج Hysteresis */
    LED_Init();          /* الـ LED الأحمر مطفي في البداية */

    /* عداد بسيط بيحدّد متى نشيك على البطارية */
    u8 batt_tick = 0u;

    /* -------------- اللوب اللانهائي --------------
     * في كل لفة:
     *   1) بنحدّث الموتورين على حسب الجويستيك الحالي
     *      (بيشتغل فيه software-PWM للحركات القُطرية)
     *   2) بنشيك على البطارية كل ~50 لفة بس
     */
    for (;;)
    {
        /* ----- التحكم: الجويستيك بيدير الموتورين ----- */
        MOTOR_Update_FromJoystick();

        /* ----- المراقبة: البطارية ----- */
        if (++batt_tick >= BATT_CHECK_DIVIDER)
        {
            batt_tick = 0u;     /* تصفير العداد */

            if (BATTERY_IsLow())
            {
                /* الجهد ضعيف → ولّع الـ LED الأحمر للتنبيه */
                LED_SetState(LED_STATE_ON);
            }
            else
            {
                /* الجهد كويس → اطفّي الـ LED */
                LED_SetState(LED_STATE_OFF);
            }
        }
    }

    /* مفروض ميوصلش هنا أبدًا */
    return 0;
}
